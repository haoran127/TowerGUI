#!/usr/bin/env node
/**
 * tower-to-proxy — Generate C# proxy classes from .tower.json dataBind annotations.
 *
 * The proxy class provides:
 *   - Bind()           — finds named UI nodes and caches references
 *   - FillFromServer()  — populates display nodes from proto data
 *   - virtual On_xxx()  — event stubs for programmer to override
 *
 * Usage:
 *   node tools/tower-to-proxy.mjs <input.tower.json> [-o output_dir] [--namespace MyGame.UI]
 *   node tools/tower-to-proxy.mjs --dir <dir> [-o output_dir] [--namespace MyGame.UI]
 */
import fs from 'fs';
import path from 'path';

function collectBindings(node, results = []) {
  if (!node || typeof node !== 'object') return results;
  if (node.dataBind) {
    results.push({
      name: node.props?.name || '',
      type: node.type,
      dataBind: node.dataBind,
    });
  }
  if (Array.isArray(node.children)) {
    for (const child of node.children) {
      if (typeof child === 'object') collectBindings(child, results);
    }
  }
  return results;
}

function toPascalCase(str) {
  return str
    .replace(/[^a-zA-Z0-9]+(.)/g, (_, c) => c.toUpperCase())
    .replace(/^./, c => c.toUpperCase());
}

function toCamelCase(str) {
  const p = toPascalCase(str);
  return p.charAt(0).toLowerCase() + p.slice(1);
}

const PROTO_TO_CS = {
  string: 'string',
  int32: 'int',
  int64: 'long',
  float: 'float',
  double: 'double',
  bool: 'bool',
  bytes: 'byte[]',
};

const TOWER_TYPE_TO_UGUI = {
  'ui-text': 'TMP_Text',
  'ui-image': 'Image',
  'ui-button': 'Button',
  'ui-input': 'TMP_InputField',
  'ui-toggle': 'Toggle',
  'ui-slider': 'Slider',
  'ui-progress': 'Slider',
  'ui-scroll': 'ScrollRect',
  'ui-view': 'RectTransform',
};

function generateProxy(doc, filePath, namespace) {
  const screenName = doc.meta?.name || path.basename(filePath, '.tower.json');
  const className = toPascalCase(screenName) + 'Proxy';
  const dataClass = toPascalCase(screenName) + 'Data';
  const bindings = collectBindings(doc.root);

  if (bindings.length === 0) return null;

  const displays = bindings.filter(b => b.dataBind.role === 'display');
  const events = bindings.filter(b => b.dataBind.role === 'event');
  const lists = bindings.filter(b => b.dataBind.role === 'list');

  const lines = [];
  lines.push('// Auto-generated by tower-to-proxy. Do not edit manually.');
  lines.push('using UnityEngine;');
  lines.push('using UnityEngine.UI;');
  lines.push('using TMPro;');
  lines.push('using System;');
  lines.push('using System.Collections.Generic;');
  lines.push('');
  lines.push(`namespace ${namespace}`);
  lines.push('{');

  // Class
  lines.push(`    public partial class ${className} : MonoBehaviour`);
  lines.push('    {');

  // Fields
  lines.push('        #region UI References');
  for (const b of bindings) {
    const uguiType = TOWER_TYPE_TO_UGUI[b.type] || 'RectTransform';
    const fieldName = toCamelCase(b.name || 'unnamed');
    lines.push(`        private ${uguiType} _${fieldName};`);
  }
  lines.push('        #endregion');
  lines.push('');

  // Bind()
  lines.push('        /// <summary>Find and cache all named UI nodes. Call once after instantiation.</summary>');
  lines.push('        public void Bind(Transform root)');
  lines.push('        {');
  for (const b of bindings) {
    const uguiType = TOWER_TYPE_TO_UGUI[b.type] || 'RectTransform';
    const fieldName = toCamelCase(b.name || 'unnamed');
    const nodeName = b.name;
    lines.push(`            _${fieldName} = root.Find("${nodeName}")?.GetComponent<${uguiType}>();`);
  }
  lines.push('');
  // Wire up event handlers
  for (const ev of events) {
    const fieldName = toCamelCase(ev.name || 'unnamed');
    const evName = ev.dataBind.event || `on${toPascalCase(ev.name || 'Action')}`;
    const handlerName = `On_${evName}`;
    if (ev.type === 'ui-button') {
      lines.push(`            _${fieldName}?.onClick.AddListener(() => ${handlerName}());`);
    } else if (ev.type === 'ui-toggle') {
      lines.push(`            _${fieldName}?.onValueChanged.AddListener((v) => ${handlerName}());`);
    }
  }
  lines.push('        }');
  lines.push('');

  // FillFromServer()
  if (displays.length > 0 || lists.length > 0) {
    lines.push(`        /// <summary>Populate display nodes from server data.</summary>`);
    lines.push(`        public void FillFromServer(${dataClass} data)`);
    lines.push('        {');
    lines.push('            if (data == null) return;');
    for (const d of displays) {
      const fieldName = toCamelCase(d.name || 'unnamed');
      const dataField = d.dataBind.field || fieldName;

      if (d.type === 'ui-text') {
        lines.push(`            if (_${fieldName} != null) _${fieldName}.text = data.${toPascalCase(dataField)}.ToString();`);
      } else if (d.type === 'ui-image') {
        lines.push(`            if (_${fieldName} != null) { /* Load sprite: data.${toPascalCase(dataField)} */ }`);
      } else if (d.type === 'ui-progress' || d.type === 'ui-slider') {
        lines.push(`            if (_${fieldName} != null) _${fieldName}.value = data.${toPascalCase(dataField)};`);
      } else if (d.type === 'ui-input') {
        lines.push(`            if (_${fieldName} != null) _${fieldName}.text = data.${toPascalCase(dataField)}?.ToString() ?? "";`);
      }
    }

    for (const l of lists) {
      const fieldName = toCamelCase(l.name || 'unnamed');
      const dataField = l.dataBind.field || fieldName;
      lines.push(`            // TODO: Populate list "${l.name}" from data.${toPascalCase(dataField)}`);
    }
    lines.push('        }');
    lines.push('');
  }

  // Event stubs
  lines.push('        #region Event Handlers (override in partial class)');
  for (const ev of events) {
    const evName = ev.dataBind.event || `on${toPascalCase(ev.name || 'Action')}`;
    const handlerName = `On_${evName}`;
    lines.push(`        protected virtual void ${handlerName}()`);
    lines.push('        {');
    lines.push(`            Debug.Log("[${className}] ${handlerName} triggered");`);
    lines.push('        }');
    lines.push('');
  }
  lines.push('        #endregion');

  // Cleanup
  lines.push('');
  lines.push('        private void OnDestroy()');
  lines.push('        {');
  for (const ev of events) {
    const fieldName = toCamelCase(ev.name || 'unnamed');
    if (ev.type === 'ui-button') {
      lines.push(`            _${fieldName}?.onClick.RemoveAllListeners();`);
    } else if (ev.type === 'ui-toggle') {
      lines.push(`            _${fieldName}?.onValueChanged.RemoveAllListeners();`);
    }
  }
  lines.push('        }');

  lines.push('    }');
  lines.push('}');

  return { screenName, className, content: lines.join('\n'), bindings };
}

function processFile(filePath, outputDir, namespace) {
  const raw = fs.readFileSync(filePath, 'utf-8');
  let doc;
  try {
    doc = JSON.parse(raw);
  } catch (e) {
    console.error(`[SKIP] Invalid JSON: ${filePath}`);
    return null;
  }
  if (doc.$schema !== 'tower-ui') {
    console.error(`[SKIP] Not a tower-ui document: ${filePath}`);
    return null;
  }

  const result = generateProxy(doc, filePath, namespace);
  if (!result) {
    console.log(`[SKIP] No dataBind annotations: ${filePath}`);
    return null;
  }

  const outPath = path.join(outputDir, `${result.className}.cs`);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, result.content, 'utf-8');
  console.log(`[OK] ${outPath} (${result.bindings.length} bindings)`);
  return result;
}

// CLI
const args = process.argv.slice(2);
let inputPath = null;
let outputDir = null;
let batchDir = null;
let namespace = 'TowerUI.Generated';

for (let i = 0; i < args.length; i++) {
  if (args[i] === '-o' && args[i + 1]) {
    outputDir = args[++i];
  } else if (args[i] === '--dir' && args[i + 1]) {
    batchDir = args[++i];
  } else if (args[i] === '--namespace' && args[i + 1]) {
    namespace = args[++i];
  } else if (!inputPath) {
    inputPath = args[i];
  }
}

if (!inputPath && !batchDir) {
  console.log('Usage: node tools/tower-to-proxy.mjs <input.tower.json> [-o output_dir] [--namespace MyGame.UI]');
  console.log('       node tools/tower-to-proxy.mjs --dir <dir> [-o output_dir] [--namespace MyGame.UI]');
  process.exit(1);
}

outputDir = outputDir || './generated/proxy';

if (batchDir) {
  const files = [];
  function walk(dir) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, entry.name);
      if (entry.isDirectory()) walk(full);
      else if (entry.name.endsWith('.tower.json')) files.push(full);
    }
  }
  walk(batchDir);
  console.log(`Found ${files.length} .tower.json files in ${batchDir}`);
  let generated = 0;
  for (const f of files) {
    if (processFile(f, outputDir, namespace)) generated++;
  }
  console.log(`Generated ${generated} proxy classes`);
} else {
  processFile(inputPath, outputDir, namespace);
}
